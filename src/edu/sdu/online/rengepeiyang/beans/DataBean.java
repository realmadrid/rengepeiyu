package edu.sdu.online.rengepeiyang.beans;

public class DataBean {
	private String dataStr;
	private String caption;
	private String subCaption;
	private String xName;
	private String yName;
	private String numberPrefix;
	private boolean decimalPrecision;
	private int ymax;
	private int ymin;
	private int numDivLines;
	public String getDataStr() {
		return dataStr;
	}
	public void setDataStr(String dataStr) {
		this.dataStr = dataStr;
	}
	public String getCaption() {
		return caption;
	}
	public void setCaption(String caption) {
		this.caption = caption;
	}
	public String getSubCaption() {
		return subCaption;
	}
	public void setSubCaption(String subCaption) {
		this.subCaption = subCaption;
	}
	public String getxName() {
		return xName;
	}
	public void setxName(String xName) {
		this.xName = xName;
	}
	public String getyName() {
		return yName;
	}
	public void setyName(String yName) {
		this.yName = yName;
	}
	public String getNumberPrefix() {
		return numberPrefix;
	}
	public void setNumberPrefix(String numberPrefix) {
		this.numberPrefix = numberPrefix;
	}
	public boolean isDecimalPrecision() {
		return decimalPrecision;
	}
	public void setDecimalPrecision(boolean decimalPrecision) {
		this.decimalPrecision = decimalPrecision;
	}
	public int getYmax() {
		return ymax;
	}
	public void setYmax(int ymax) {
		this.ymax = ymax;
	}
	public int getYmin() {
		return ymin;
	}
	public void setYmin(int ymin) {
		this.ymin = ymin;
	}
	public int getNumDivLines() {
		return numDivLines;
	}
	public void setNumDivLines(int numDivLines) {
		this.numDivLines = numDivLines;
	}
	
	
}
